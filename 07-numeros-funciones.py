import math  # math es un modulo con funciones matematicas
print(round(1.3))   # round() sirve para redondear
print(round(1.7))
print(round(1.5))
print(abs(-77))
print(abs(55))


print(math.ceil(1.1))  # lleva al numero entero mayor mas proximo
print(math.floor(1.9999))  # lleva al numero entero menor mas proximo
print(math.isnan(23))    # si el valor no es un numero devuelve boolean
# print(math.isnan("23"))
print(math.pow(10, 3))   # eleva a la potencia (10 elevado a la 3)
print(math.sqrt(9))  # raiz cuadrada
